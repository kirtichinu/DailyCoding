//ABSTRACT DATA TYPES

// Abstract Data Types (ADTs) are a fundamental concept in computer science and programming. They are a theoretical model of a data structure that specifies the type of data stored, the operations that can be performed on the data, and the behavior of these operations, but not the implementation details. ADTs provide a high-level way to think about how data can be organized and manipulated.



// //Examples of Abstract Data Types

//                          |
//                          *

//     List:
//         Operations: Insert, Delete, Find, Traverse.
//         Description: A collection of elements, where each element has a specific position.

//     Stack:
//         Operations: Push (add an element), Pop (remove an element), Peek (view the top element).
//         Description: A collection of elements with Last-In-First-Out (LIFO) access.

//     Queue:
//         Operations: Enqueue (add an element), Dequeue (remove an element), Front (view the front element).
//         Description: A collection of elements with First-In-First-Out (FIFO) access.

//     Tree:
//         Operations: Insert, Delete, Search, Traverse.
//         Description: A hierarchical structure with nodes, where each node has zero or more child nodes and exactly one parent node (except for the root).

//     Graph:
//         Operations: Add Vertex, Add Edge, Remove Vertex, Remove Edge, Search (e.g., DFS, BFS).
//         Description: A collection of nodes (vertices) and edges connecting pairs of nodes.

//     Hash Table:
//         Operations: Insert, Delete, Search.
//         Description: A collection of key-value pairs, where each key is unique and is used to compute an index into an array of buckets or slots from which the desired value can be found.
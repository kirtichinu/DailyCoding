#include<stdio.h>
int main()
{
    //Variable declaration
    //  integer

    int a=1;
    int b=2;
    //  constant
    char c='*';
    char d='#';
    //  float/decimal 
    float e=4.9;
    float f=5.9;

    //Constant declaration
    //  integer constant
    const int g=6;
    //  real constant
    const float h= 6.9;
    //  character constant
    const char i='e';

    printf("a=%d\n b=%d\n c=%c\n d=%c\n e=%f\n f=%f\n g=%d\n h=%f\n i=%c\n", a,b,c,d,e,f,g,h,i);

    return 0;
}
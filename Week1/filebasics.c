/*
file I/O basics
eof= end of file
fopen()= open a file
fclose()= close a file
operations on files include --->> create, open, close, read, write 
types of files --->> text file i.e. .txt , binary file i.e. .bin
file opening modes --->> 
r= open to read 
rb= open to read in binary
w= open to write
wb= open to write  in binary
a= open to append (adds a char again)

fgetc(fptr) --->> READ
fputc('A' , fptr) --->> WRITE
*/